class SwapTwoNumbers{

      public static void main(String anu[]){
	  
		int x = 20;
		int y = 25;
		
		System.out.println("Before the swap");
		System.out.println("x is " + x);
		System.out.println("y is " + y);
		int temp = x;
		x = y;
		y = temp;
		
		System.out.println("After the swap");
		System.out.println("x is " + x);
		System.out.println("y is " + y);
	  
	}

}